from enum import IntEnum


class SQLTinyInt(IntEnum):
    SIGNED_MIN = -128
    SIGNED_MAX = 127
    UNSIGNED_MIN = 0
    UNSIGNED_MAX = 255


class SQLSmallInt(IntEnum):
    SIGNED_MIN = -32768
    SIGNED_MAX = 32767
    UNSIGNED_MIN = 0
    UNSIGNED_MAX = 65535


class SQLMediumInt(IntEnum):
    SIGNED_MIN = -8388608
    SIGNED_MAX = 8388607
    UNSIGNED_MIN = 0
    UNSIGNED_MAX = 16777215


class SQLInt(IntEnum):
    SIGNED_MIN = -2147483648
    SIGNED_MAX = 2147483647
    UNSIGNED_MIN = 0
    UNSIGNED_MAX = 4294967295


class SQLBigInt(IntEnum):
    SIGNED_MIN = -9223372036854775808
    SIGNED_MAX = 9223372036854775807
    UNSIGNED_MIN = 0
    UNSIGNED_MAX = 18446744073709551615
